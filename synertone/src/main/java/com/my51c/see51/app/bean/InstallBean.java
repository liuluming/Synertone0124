package com.my51c.see51.app.bean;

public class InstallBean {


    private String curstep;

    private String dur;

    private String progress;

    private String err;

    private String bucoffset;

    private String rfCert;

    public String getCurstep() {
        return curstep;
    }

    public void setCurstep(String curstep) {
        this.curstep = curstep;
    }

    public String getDur() {
        return dur;
    }

    public void setDur(String dur) {
        this.dur = dur;
    }

    public String getProgress() {
        return progress;
    }

    public void setProgress(String progress) {
        this.progress = progress;
    }

    public String getErr() {
        return err;
    }

    public void setErr(String err) {
        this.err = err;
    }

    public String getBucoffset() {
        return bucoffset;
    }

    public void setBucoffset(String bucoffset) {
        this.bucoffset = bucoffset;
    }

    public String getRfCert() {
        return rfCert;
    }

    public void setRfCert(String rfCert) {
        this.rfCert = rfCert;
    }
}


