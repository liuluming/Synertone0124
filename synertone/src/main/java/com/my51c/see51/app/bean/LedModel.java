package com.my51c.see51.app.bean;

public class LedModel {
    String power;
    String satnet;
    String satlink;
    String tx;
    String rx;

    public String getPower() {
        return power;
    }

    public void setPower(String power) {
        this.power = power;
    }

    public String getSatnet() {
        return satnet;
    }

    public void setSatnet(String satnet) {
        this.satnet = satnet;
    }

    public String getSatlink() {
        return satlink;
    }

    public void setSatlink(String satlink) {
        this.satlink = satlink;
    }

    public String getTx() {
        return tx;
    }

    public void setTx(String tx) {
        this.tx = tx;
    }

    public String getRx() {
        return rx;
    }

    public void setRx(String rx) {
        this.rx = rx;
    }

}
