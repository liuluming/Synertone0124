package com.my51c.see51.app.domian;

public class Sateenable {

    private String satenum;
    private String satelon;
    private String mode;
    private String freq;
    private String bw;
    private String type;
    private String modem;

    public String getSatenum() {
        return satenum;
    }

    public void setSatenum(String satenum) {
        this.satenum = satenum;
    }

    public String getSatelon() {
        return satelon;
    }

    public void setSatelon(String satelon) {
        this.satelon = satelon;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public String getFreq() {
        return freq;
    }

    public void setFreq(String freq) {
        this.freq = freq;
    }

    public String getBw() {
        return bw;
    }

    public void setBw(String bw) {
        this.bw = bw;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getModem() {
        return modem;
    }

    public void setModem(String modem) {
        this.modem = modem;
    }


}
