package com.my51c.see51.app.domian;

public class WhileList {

    public String enable;
    public String ip;
    public String mac;
    public String user;
    public boolean whileListTag;
    public int whileTag;

    public int getWhileTag() {
        return whileTag;
    }

    public void setWhileTag(int whileTag) {
        this.whileTag = whileTag;
    }

    public boolean isWhileListTag() {
        return whileListTag;
    }

    public void setWhileListTag(boolean whileListTag) {
        this.whileListTag = whileListTag;
    }

    public String getEnable() {
        return enable;
    }

    public void setEnable(String enable) {
        this.enable = enable;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }


}
